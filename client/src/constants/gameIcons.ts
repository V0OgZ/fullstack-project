// Fonction pour obtenir une icône aléatoire d'une catégorie
export const getRandomIconFromCategory = (category: keyof typeof ICON_CATEGORIES): GameIconValue => {
  const categoryKeys = ICON_CATEGORIES[category];
  const randomKey = categoryKeys[Math.floor(Math.random() * categoryKeys.length)];
  return GAME_ICONS[randomKey];
};

// Fonction pour obtenir toutes les icônes d'une catégorie
export const getIconsFromCategory = (category: keyof typeof ICON_CATEGORIES): GameIconValue[] => {
  const categoryKeys = ICON_CATEGORIES[category];
  return categoryKeys.map(key => GAME_ICONS[key]);
}; 