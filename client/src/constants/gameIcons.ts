// Types pour TypeScript
export type GameIconKey = keyof typeof GAME_ICONS;
export type GameIconValue = typeof GAME_ICONS[GameIconKey];

// Fonctions utilitaires
export const getIcon = (key: GameIconKey): GameIconValue => {
  return GAME_ICONS[key];
};

export const getRandomIcon = (): GameIconValue => {
  const keys = Object.keys(GAME_ICONS) as GameIconKey[];
  const randomKey = keys[Math.floor(Math.random() * keys.length)];
  return GAME_ICONS[randomKey];
};

